use std::path::Path;
use std::process::Stdio;

use crate::config::{AgentEngine, SwarmConfig};
use crate::error::SwarmError;
use crate::types::adversarial::AdversarialChallenge;

/// Generate a composite eval script from adversarial challenges.
///
/// The script contains two sections:
/// 1. **`PASS_TO_PASS`** — regression checks that must keep passing (build, existing tests)
/// 2. **`FAIL_TO_PASS`** — challenge-specific assertions that should pass after recovery
///
/// The script outputs `PASS: n/total` and individual assertion results.
///
/// If an LLM is available, challenge assertions are generated by the LLM
/// (which can inspect the actual codebase). Otherwise, falls back to
/// category-based heuristic assertions.
pub async fn generate_eval_script(
    config: &SwarmConfig,
    challenges: &[AdversarialChallenge],
    working_dir: &Path,
    severity_threshold: f64,
) -> Result<String, SwarmError> {
    let actionable: Vec<&AdversarialChallenge> = challenges
        .iter()
        .filter(|c| c.severity_score >= severity_threshold)
        .collect();

    // Try LLM-generated assertions first (more precise).
    let challenge_assertions =
        match generate_assertions_via_llm(config, &actionable, working_dir).await {
            Ok(assertions) if !assertions.is_empty() => assertions,
            Ok(_) | Err(_) => {
                tracing::debug!("LLM assertion generation unavailable, using heuristics");
                generate_heuristic_assertions(&actionable, working_dir)
            }
        };

    let regression_checks = generate_regression_checks(working_dir);

    Ok(build_script(&regression_checks, &challenge_assertions))
}

/// A single binary assertion: name + shell command that exits 0 on pass.
struct Assertion {
    name: String,
    command: String,
}

// ── Script builder ─────────────────────────────────────────────────────────────

fn build_script(regression: &[Assertion], challenge: &[Assertion]) -> String {
    let mut lines = vec![
        "#!/bin/sh".to_string(),
        "# Auto-generated eval script (SWE-bench style)".to_string(),
        "# PASS_TO_PASS: regression checks | FAIL_TO_PASS: challenge assertions".to_string(),
        "TOTAL=0; PASS=0; FAIL_DETAILS=\"\"".to_string(),
        String::new(),
        "check() {".to_string(),
        "  TOTAL=$((TOTAL+1))".to_string(),
        "  if eval \"$2\" > /dev/null 2>&1; then".to_string(),
        "    PASS=$((PASS+1))".to_string(),
        "  else".to_string(),
        "    FAIL_DETAILS=\"${FAIL_DETAILS}FAIL: $1\\n\"".to_string(),
        "  fi".to_string(),
        "}".to_string(),
        String::new(),
    ];

    if !regression.is_empty() {
        lines.push("# === PASS_TO_PASS (regression) ===".to_string());
        for a in regression {
            lines.push(format!(
                "check \"{}\" \"{}\"",
                escape_sh(&a.name),
                escape_sh(&a.command)
            ));
        }
        lines.push(String::new());
    }

    if !challenge.is_empty() {
        lines.push("# === FAIL_TO_PASS (challenge fixes) ===".to_string());
        for a in challenge {
            lines.push(format!(
                "check \"{}\" \"{}\"",
                escape_sh(&a.name),
                escape_sh(&a.command)
            ));
        }
        lines.push(String::new());
    }

    lines.push("# Output score signals".to_string());
    lines.push("printf \"%b\" \"$FAIL_DETAILS\"".to_string());
    lines.push("echo \"PASS: $PASS/$TOTAL\"".to_string());
    lines.push(String::new());
    // Exit 0 even if some checks fail — the PASS: signal carries the score.
    lines.push("exit 0".to_string());

    lines.join("\n")
}

// ── Regression checks ──────────────────────────────────────────────────────────

fn generate_regression_checks(working_dir: &Path) -> Vec<Assertion> {
    let mut checks = Vec::new();

    // Detect project type and add appropriate build checks.
    if working_dir.join("Cargo.toml").exists() || has_child_cargo(working_dir) {
        let cargo_dir = find_cargo_dir(working_dir);
        checks.push(Assertion {
            name: "rust-build".into(),
            command: format!("cd {cargo_dir} && cargo check 2>&1"),
        });
    }

    if working_dir.join("package.json").exists() || has_child_package_json(working_dir) {
        let npm_dir = find_npm_dir(working_dir);
        checks.push(Assertion {
            name: "npm-build".into(),
            command: format!("cd {npm_dir} && npx tsc --noEmit 2>&1 || npm run build 2>&1"),
        });
    }

    if working_dir.join("pyproject.toml").exists() || working_dir.join("setup.py").exists() {
        checks.push(Assertion {
            name: "python-syntax".into(),
            command: format!(
                "python3 -m py_compile $(find {} -name '*.py' -not -path '*/venv/*' | head -20) 2>&1",
                working_dir.display()
            ),
        });
    }

    // If nothing detected, at least check the directory exists.
    if checks.is_empty() {
        checks.push(Assertion {
            name: "workspace-exists".into(),
            command: format!("test -d {}", working_dir.display()),
        });
    }

    checks
}

// ── LLM-generated assertions ───────────────────────────────────────────────────

async fn generate_assertions_via_llm(
    config: &SwarmConfig,
    challenges: &[&AdversarialChallenge],
    working_dir: &Path,
) -> Result<Vec<Assertion>, SwarmError> {
    if challenges.is_empty() {
        return Ok(Vec::new());
    }

    let challenge_desc: Vec<String> = challenges
        .iter()
        .map(|c| {
            let fix = c.suggested_fix.as_deref().unwrap_or("none");
            format!(
                "- [{id}] ({cat}): {desc} | Suggested: {fix}",
                id = c.id,
                cat = c.category,
                desc = c.description,
            )
        })
        .collect();

    let prompt = format!(
        r#"Generate binary test assertions for a code eval harness. Each assertion is a shell command that exits 0 if the issue is fixed.

## Working Directory
{working_dir}

## Issues to Verify
{challenges}

## Rules
- Each assertion must be a SINGLE shell command (grep, test, cargo, etc.)
- Exit code 0 = pass, non-zero = fail
- Use grep -q for pattern checks, test -f for file existence
- Be specific to the codebase (use actual file paths and function names)
- 1 assertion per issue, max 6 total
- NO subjective checks ("code is clean") — only executable binary checks

## Output Format
One assertion per line: NAME|COMMAND
Example:
persistent-kernel|grep -q 'Child\|stdin\|persistent' {working_dir}/backend/src/kernel.rs
input-validation|grep -rq 'validate\|sanitize\|is_empty' {working_dir}/backend/src/

Output ONLY assertion lines, nothing else."#,
        working_dir = working_dir.display(),
        challenges = challenge_desc.join("\n"),
    );

    let engine = config.defaults.engine;
    let cmd_name = match engine {
        AgentEngine::Claude => "claude",
        AgentEngine::Gemini => "gemini",
    };

    let mut cmd = tokio::process::Command::new(cmd_name);
    cmd.arg("-p")
        .arg(&prompt)
        .arg("--output-format")
        .arg("text");

    match engine {
        AgentEngine::Claude => {
            cmd.arg("--model").arg("haiku");
        }
        AgentEngine::Gemini => {
            cmd.arg("--yolo");
        }
    }

    let result = tokio::time::timeout(
        std::time::Duration::from_secs(60),
        cmd.stdout(Stdio::piped()).stderr(Stdio::piped()).output(),
    )
    .await;

    let output = match result {
        Ok(Ok(o)) if o.status.success() => String::from_utf8_lossy(&o.stdout).to_string(),
        _ => {
            return Err(SwarmError::EvalHarness(
                "LLM assertion generation failed".into(),
            ));
        }
    };

    Ok(parse_assertion_lines(&output))
}

fn parse_assertion_lines(output: &str) -> Vec<Assertion> {
    output
        .lines()
        .filter_map(|line| {
            let line = line.trim();
            if line.is_empty() || line.starts_with('#') || line.starts_with("Example") {
                return None;
            }
            let (name, command) = line.split_once('|')?;
            let name = name.trim();
            let command = command.trim();
            if name.is_empty() || command.is_empty() {
                return None;
            }
            Some(Assertion {
                name: name.to_string(),
                command: command.to_string(),
            })
        })
        .take(6) // Cap at 6 assertions.
        .collect()
}

// ── Heuristic assertions ───────────────────────────────────────────────────────

fn generate_heuristic_assertions(
    challenges: &[&AdversarialChallenge],
    working_dir: &Path,
) -> Vec<Assertion> {
    challenges
        .iter()
        .take(6)
        .filter_map(|c| heuristic_for_challenge(c, working_dir))
        .collect()
}

fn heuristic_for_challenge(
    challenge: &AdversarialChallenge,
    working_dir: &Path,
) -> Option<Assertion> {
    let dir = working_dir.display();
    let desc_lower = challenge.description.to_lowercase();

    // Category + keyword matching for common patterns.
    match challenge.category.as_str() {
        "correctness" => {
            if desc_lower.contains("stateless")
                || desc_lower.contains("persistent")
                || desc_lower.contains("kernel")
            {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!("grep -rq 'stdin\\|persistent\\|Child\\|spawn' {dir}/"),
                })
            } else if desc_lower.contains("error") || desc_lower.contains("handling") {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!(
                        "grep -rq 'Result\\|Error\\|unwrap_or\\|map_err\\|\\?' {dir}/"
                    ),
                })
            } else {
                None
            }
        }
        "security" => {
            if desc_lower.contains("validation")
                || desc_lower.contains("sanitiz")
                || desc_lower.contains("inject")
            {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!(
                        "grep -rq 'validate\\|sanitize\\|is_empty\\|trim\\|escape' {dir}/"
                    ),
                })
            } else if desc_lower.contains("sandbox")
                || desc_lower.contains("limit")
                || desc_lower.contains("timeout")
            {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!("grep -rq 'timeout\\|limit\\|sandbox\\|kill\\|SIGINT' {dir}/"),
                })
            } else {
                None
            }
        }
        "performance" => {
            if desc_lower.contains("n+1") || desc_lower.contains("query") {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!("grep -rq 'batch\\|bulk\\|join\\|prefetch\\|IN (' {dir}/"),
                })
            } else if desc_lower.contains("blocking") || desc_lower.contains("async") {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!(
                        "grep -rq 'async\\|await\\|tokio::fs\\|spawn_blocking' {dir}/"
                    ),
                })
            } else {
                None
            }
        }
        "completeness" => {
            if desc_lower.contains("keyboard") || desc_lower.contains("navigation") {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!(
                        "grep -rq 'onKeyDown\\|keydown\\|ArrowUp\\|ArrowDown\\|keyboard' {dir}/"
                    ),
                })
            } else if desc_lower.contains("interrupt") || desc_lower.contains("cancel") {
                Some(Assertion {
                    name: format!("fix-{}", challenge.id),
                    command: format!(
                        "grep -rq 'interrupt\\|cancel\\|abort\\|SIGINT\\|kill' {dir}/"
                    ),
                })
            } else {
                None
            }
        }
        _ => None,
    }
}

// ── Helpers ────────────────────────────────────────────────────────────────────

fn escape_sh(s: &str) -> String {
    s.replace('\'', "'\\''")
}

fn has_child_cargo(dir: &Path) -> bool {
    ["backend", "server", "api", "src"]
        .iter()
        .any(|d| dir.join(d).join("Cargo.toml").exists())
}

fn has_child_package_json(dir: &Path) -> bool {
    ["frontend", "client", "web", "ui", "app"]
        .iter()
        .any(|d| dir.join(d).join("package.json").exists())
}

fn find_cargo_dir(dir: &Path) -> String {
    for sub in ["backend", "server", "api"] {
        if dir.join(sub).join("Cargo.toml").exists() {
            return format!("{}/{sub}", dir.display());
        }
    }
    dir.display().to_string()
}

fn find_npm_dir(dir: &Path) -> String {
    for sub in ["frontend", "client", "web", "ui", "app"] {
        if dir.join(sub).join("package.json").exists() {
            return format!("{}/{sub}", dir.display());
        }
    }
    dir.display().to_string()
}

#[cfg(test)]
mod tests {
    use super::*;
    use crate::types::adversarial::ChallengeSeverity;

    fn make_challenge(id: &str, category: &str, description: &str) -> AdversarialChallenge {
        AdversarialChallenge {
            id: id.into(),
            target_task_id: None,
            category: category.into(),
            description: description.into(),
            severity: ChallengeSeverity::High,
            severity_score: 0.75,
            suggested_fix: None,
            resolved: false,
            resolution: None,
        }
    }

    #[test]
    fn test_parse_assertion_lines() {
        let output = "persistent-kernel|grep -q 'Child' backend/src/kernel.rs\ninput-validation|grep -rq 'validate' src/\n";
        let assertions = parse_assertion_lines(output);
        assert_eq!(assertions.len(), 2);
        assert_eq!(assertions[0].name, "persistent-kernel");
        assert!(assertions[0].command.contains("grep"));
    }

    #[test]
    fn test_parse_skips_comments_and_empty() {
        let output = "# comment\n\nname|command\n  \nExample: foo|bar\n";
        let assertions = parse_assertion_lines(output);
        assert_eq!(assertions.len(), 1);
        assert_eq!(assertions[0].name, "name");
    }

    #[test]
    fn test_parse_caps_at_six() {
        let output = (1..=10)
            .map(|i| format!("check-{i}|test -f /tmp/file{i}"))
            .collect::<Vec<_>>()
            .join("\n");
        let assertions = parse_assertion_lines(&output);
        assert_eq!(assertions.len(), 6);
    }

    #[test]
    fn test_heuristic_correctness_stateless() {
        let c = make_challenge(
            "c-1",
            "correctness",
            "Kernel is stateless, variables lost between cells",
        );
        let a = heuristic_for_challenge(&c, Path::new("/tmp/project"));
        assert!(a.is_some());
        assert!(a.unwrap().command.contains("persistent"));
    }

    #[test]
    fn test_heuristic_security_validation() {
        let c = make_challenge("s-1", "security", "No input validation on user data");
        let a = heuristic_for_challenge(&c, Path::new("/tmp/project"));
        assert!(a.is_some());
        assert!(a.unwrap().command.contains("validate"));
    }

    #[test]
    fn test_heuristic_performance_blocking() {
        let c = make_challenge(
            "p-1",
            "performance",
            "Uses blocking std::fs IO on async executor",
        );
        let a = heuristic_for_challenge(&c, Path::new("/tmp/project"));
        assert!(a.is_some());
        assert!(a.unwrap().command.contains("tokio::fs"));
    }

    #[test]
    fn test_heuristic_unknown_returns_none() {
        let c = make_challenge("x-1", "style", "Variable naming is inconsistent");
        let a = heuristic_for_challenge(&c, Path::new("/tmp/project"));
        assert!(a.is_none());
    }

    #[test]
    fn test_build_script_structure() {
        let regression = vec![Assertion {
            name: "rust-build".into(),
            command: "cargo check".into(),
        }];
        let challenge = vec![Assertion {
            name: "fix-c1".into(),
            command: "grep -q 'persistent' src/kernel.rs".into(),
        }];
        let script = build_script(&regression, &challenge);
        assert!(script.contains("#!/bin/sh"));
        assert!(script.contains("PASS_TO_PASS"));
        assert!(script.contains("FAIL_TO_PASS"));
        assert!(script.contains("cargo check"));
        assert!(script.contains("grep -q"));
        assert!(script.contains("PASS: $PASS/$TOTAL"));
    }

    #[test]
    fn test_regression_checks_detect_rust() {
        let dir = std::env::temp_dir().join("eval_gen_test_rust");
        std::fs::create_dir_all(dir.join("backend")).ok();
        std::fs::write(dir.join("backend/Cargo.toml"), "[package]\nname=\"test\"").ok();
        let checks = generate_regression_checks(&dir);
        assert!(checks.iter().any(|c| c.name == "rust-build"));
        std::fs::remove_dir_all(&dir).ok();
    }
}
